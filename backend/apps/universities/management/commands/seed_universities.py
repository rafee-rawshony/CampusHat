class Command(BaseCommand):
    help = 'Seed Bangladesh universities, colleges, and institutes for student verification.'

    def add_arguments(self, parser):
        parser.add_argument(
            '--update',
            action='store_true',
            help='Update existing rows (otherwise skip if name already exists).',
        )

    @transaction.atomic
    def handle(self, *args, **options):
        update_existing = options.get('update', False)
        created_count = 0
        updated_count = 0
        restored_count = 0
        skipped_count = 0

        # Quick duplicate-checks within seed data — fail fast.
        seen_shorts = {}
        seen_slugs = {}
        for row in INSTITUTIONS:
            short = row[1]
            if short in seen_shorts:
                self.stderr.write(self.style.ERROR(
                    f'Duplicate short_name "{short}" in seed data: '
                    f'"{seen_shorts[short]}" vs "{row[0]}"'
                ))
                return
            seen_shorts[short] = row[0]

            generated_slug = slugify(short)
            if generated_slug in seen_slugs:
                self.stderr.write(self.style.ERROR(
                    f'Duplicate slug "{generated_slug}" from short_name: '
                    f'"{seen_slugs[generated_slug]}" vs "{row[0]}"'
                ))
                return
            seen_slugs[generated_slug] = row[0]

        for name, short_name, division, district, postal_code, full_address, email_domain in INSTITUTIONS:
            generated_slug = slugify(short_name)
            defaults = {
                'short_name': short_name,
                'division': division,
                'district': district,
                'postal_code': postal_code,
                'full_address': full_address,
                'email_domain': email_domain,
                'is_active': True,
            }

            # Match by strongest identity first, then fall back carefully.
            obj = University.all_objects.filter(name=name).first()
            if obj is None:
                obj = University.all_objects.filter(short_name=short_name).first()
                if obj is not None and obj.name != name:
                    self.stderr.write(self.style.ERROR(
                        f'short_name "{short_name}" already belongs to "{obj.name}", '
                        f'cannot seed "{name}". Please resolve this conflict first.'
                    ))
                    return

            if obj is None:
                obj = University.all_objects.filter(slug=generated_slug).first()
                if obj is not None and obj.name != name and obj.short_name != short_name:
                    self.stderr.write(self.style.ERROR(
                        f'slug "{generated_slug}" already belongs to "{obj.name}" '
                        f'({obj.short_name}), cannot seed "{name}" ({short_name}). '
                        'Please resolve this conflict first.'
                    ))
                    return

            if obj is None:
                University.objects.create(name=name, **defaults)
                created_count += 1
                continue

            # If the institution exists but is soft-deleted, re-activate it.
            if obj.deleted_at is not None:
                obj.restore()
                restored_count += 1

            if update_existing:
                # Only update non-identity fields; never touch slug/system_id.
                for field, value in defaults.items():
                    setattr(obj, field, value)
                obj.save()
                updated_count += 1
            else:
                skipped_count += 1

        total = University.objects.count()
        self.stdout.write(self.style.SUCCESS(
            f'Done. Created: {created_count} | Updated: {updated_count} | '
            f'Restored: {restored_count} | Skipped (existing): {skipped_count} | '
            f'Total in DB: {total}'
        ))
